package es.ligasnba.app.model.partido;

import java.math.BigDecimal;

public class ResumenValoraciones{

	private BigDecimal valoracionMedia;
	private short numeroValoraciones;
	
	public BigDecimal getValoracionMedia() {
		return valoracionMedia;
	}
	public void setValoracionMedia(BigDecimal valoracionMedia) {
		this.valoracionMedia = valoracionMedia;
	}
	public short getNumeroValoraciones() {
		return numeroValoraciones;
	}
	public void setNumeroValoraciones(short numeroValoraciones) {
		this.numeroValoraciones = numeroValoraciones;
	}
	
	
	
	
}
